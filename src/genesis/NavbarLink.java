package genesis;

public class NavbarLink {
    private String path, link, linkInsert;

    public String getPath() {
        return path;
    }

    public void setPath(String path) {
        this.path = path;
    }

    public String getLink() {
        return link;
    }

    public void setLink(String link) {
        this.link = link;
    }

    public String getLinkInsert() {
        return linkInsert;
    }

    public void setLinkInsert(String linkInsert) {
        this.linkInsert = linkInsert;
    }
    
}
