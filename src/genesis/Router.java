package genesis;

public class Router {
    String path;
    String componentImportList;
    String componentImportInsert;
    String componentImportUpdate;
    String linkList;
    String linkInsert;
    String linkUpdate;
    public String getPath() {
        return path;
    }
    public void setPath(String path) {
        this.path = path;
    }
    public String getLinkList() {
        return linkList;
    }
    public void setLinkList(String linkList) {
        this.linkList = linkList;
    }
    public String getLinkInsert() {
        return linkInsert;
    }
    public void setLinkInsert(String linkInsert) {
        this.linkInsert = linkInsert;
    }
    public String getLinkUpdate() {
        return linkUpdate;
    }
    public void setLinkUpdate(String linkUpdate) {
        this.linkUpdate = linkUpdate;
    }
    public String getComponentImportList() {
        return componentImportList;
    }
    public void setComponentImportList(String componentImportList) {
        this.componentImportList = componentImportList;
    }
    public String getComponentImportInsert() {
        return componentImportInsert;
    }
    public void setComponentImportInsert(String componentImportInsert) {
        this.componentImportInsert = componentImportInsert;
    }
    public String getComponentImportUpdate() {
        return componentImportUpdate;
    }
    public void setComponentImportUpdate(String componentImportUpdate) {
        this.componentImportUpdate = componentImportUpdate;
    }
}
